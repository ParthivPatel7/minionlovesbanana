package reverseword;

/**
 *
 * @author ppart
 */
public class Student {
       private String name;
    private int age;
    
    Student(int a, String str){
        age = a;
        name = str;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }

}
